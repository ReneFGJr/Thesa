<li>Caixa alta / baixa
<p>
Um estilo consistente deve ser usado para a apresentação dos termos do dicionário de sinônimos. 
Esta parte da ISO 25964 usa minúsculas em todo, exceto onde são necessários maiúsculas e minúsculas em nomes próprios. 
Como os computadores não são mais restritos ao uso de maiúsculas somente, deve-se usar minúsculas ou minúsculas com letras maiúsculas iniciais.

Podem ser feitas exceções para abreviaturas, acrônimos, nomes próprios ou termos que são convencionalmente escritos em um estilo especial. 
Nestes casos, o estilo que é mais amplamente aceito entre os usuários antecipados do dicionário de sinônimos deve ser usado.
</p>
