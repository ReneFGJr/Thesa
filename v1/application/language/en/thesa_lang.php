$lang['context_01_section'] = 'Presentation of Thesa';
$lang['context_01'] = '
<p> Thesa was developed with the purpose of providing an instrument for undergraduate students of library science in the discipline of Documentary Languages ​​for the development of thesaurus, in order to make it possible to reduce operational work and give greater attention to the work of cognitive and conceptual development related to domain modeling. </p>
<p> As a guide for the application, it was based on the ISO and NISO standards in force, in order to make its guidelines compatible with the pressing semantic requirements in the new demands of the SOCs. Based on the available literature, the ISO and NISO thesaurus construction standards identified the elements necessary for the development of the prototype, especially with regard to the survey of the connection properties between the concepts. </p>
<p> Thesa´s structure is based on the conception of the relationships between concepts, based on the assumption that a concept can be represented by a term, an image, a sound, a link or any other form that can be explained. In this approach, the concept is perennial, while its representation can vary according to the historical or social context, being defined a preferential form, and numerous alternative and hidden forms. </p>
<p> How to quote:  GABRIEL JUNIOR, R. F.; LAIPELT, R. C. Thesa: ferramenta para construção de tesauro semântico aplicado interoperável. <b>Revista P2P & Inovação</b>, Rio de Janeiro, v. 3, n. 2, p.124-145, Mar./Set. 2017.</p>
';

$lang['context_02_section'] = 'About the Thesa';
$lang['context_02'] = '
<p> Thesa was initially developed as a prototype using the php language and MySql database, in order to enable the sharing and collaborative development of the tool. </p>
<p> The software works in a Web environment and can be downloaded free of charge and can be used for teaching purposes in undergraduate and graduate courses or for professional use. Thesa was developed with the principle of multi-language, being able to be translated into any language, however its test version is only in Portuguese, the translations will depend on establishing agreements with native institutions of other languages, which show interest in the use of the software. </p>
<p> Thesa uses a concept of multiple thesaurus, or multiple schemes, that is, the user can create an unlimited number of thesaurus in different areas of knowledge, the users / developers of these thesaurus, can leave them for public or private use , allowing the access of other users. In Thesa we started with the URI concept, used by SKOS and systems based on the Semantic Web, that is, each concept is associated with a permanent address on the Internet and with a unique identifier of the concept, which is represented by terms through properties . </p>';

